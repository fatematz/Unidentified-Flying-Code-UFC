<input 
      type="text" 
      placeholder="Search friends..." 
      className="border-2 border-gray-200 ml-[20px] my-[20px] p-2 rounded-2xl max-w-[400px] w-full"
      onChange={(e) => setSearchQuery(e.target.value)} 
/>
    

// এখানে (e) হলো একটি Event Object। যখনই আপনি ইনপুট ফিল্ডে কিছু টাইপ করেন, ব্রাউজার স্বয়ংক্রিয়ভাবে একটি অবজেক্ট তৈরি করে যেখানে ওই ইভেন্টের সব তথ্য থাকে।

// নিচে ফাংশনটির প্রতিটি অংশের ব্যাখ্যা দেওয়া হলো:

// ১. e বা event কী?
// যখন আপনি কিবোর্ডে কোনো কি (key) চাপেন, তখন একটি "change" ইভেন্ট ঘটে। ব্রাউজার এই ইভেন্টের বিস্তারিত তথ্য e নামক প্যারামিটারের মাধ্যমে ফাংশনে পাঠায়।

// ২. e.target কী?
// e.target মানে হলো— "কোন জিনিসের ওপর ইভেন্টটি ঘটেছে?"। এখানে ইভেন্টটি ঘটেছে আপনার <input> ট্যাগটির ওপর। তাই e.target লিখলে সরাসরি ওই ইনপুট ট্যাগটিকে বোঝানো হয়।

// ৩. e.target.value কী?
// এটি হলো ইনপুট বক্সের ভেতরে বর্তমানে যা লেখা আছে সেই টেক্সট। আপনি প্রতিবার নতুন কোনো অক্ষর টাইপ করলে e.target.value বদলে যায় এবং লেটেস্ট লেখাটি পাওয়া যায়।



const filteredData = data.filter((item) =>
  item.name.toLowerCase().includes(searchQuery.toLowerCase())
);


// ১. data.filter(...): জাভাস্ক্রিপ্টের এই মেথডটি আপনার মেইন অ্যারে থেকে একটি নতুন অ্যারে তৈরি করে। এটি অরিজিনাল data-কে পরিবর্তন করে না, শুধু শর্ত সাপেক্ষে কিছু ডেটা বেছে নেয়।

// ২. item: এটি ফিল্টার লুপের ভেতরের প্রতিটি সিঙ্গেল অবজেক্ট (আপনার JSON-এর একেকটি ফ্রেন্ড বা ফুড আইটেম)।

// ৩. .toLowerCase(): এটি খুব গুরুত্বপূর্ণ। ইউজার যদি বড় হাতের "A" লিখে সার্চ করে আর আপনার ডেটায় যদি ছোট হাতের "a" থাকে, তবে যেন মিল খুঁজে পায়। তাই আমরা সার্চের লেখা এবং ডেটার লেখা—উভয়কেই ছোট হাতের অক্ষরে রূপান্তর করে চেক করি।

// ৪. .includes(searchQuery): এটি চেক করে যে, আপনার ফ্রেন্ডের নামের ভেতর সার্চ বক্সে লেখা অক্ষরগুলো আছে কি না।

// ধরুন নাম: "Fatema", সার্চ বক্স: "fa"।

// যেহেতু "Fatema"-এর ভেতর "fa" আছে, তাই এটি true রিটার্ন করবে এবং এই আইটেমটি ফিল্টার লিস্টে থাকবে।